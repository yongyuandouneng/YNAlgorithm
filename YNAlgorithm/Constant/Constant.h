// 通用排序数组_C
static int array[5] = {4, 6, 8, 5, 9};
// 通用排序数组大小_C
static int arrayCount = 5;
// 打印
void printArray(){
    
    for (int i = 0; i < arrayCount; i++) {
        printf("%zd ",array[i]);
    }
    printf("\n");
}

